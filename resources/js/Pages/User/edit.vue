<script>
import {
  Disclosure,
  DisclosureButton,
  DisclosurePanel,
  Menu,
  MenuButton,
  MenuItem,
  MenuItems,
} from "@headlessui/vue";
import { BellIcon, MenuIcon, XIcon } from "@heroicons/vue/outline";
import { Head, Link } from "@inertiajs/inertia-vue3";

const navigation = [
  { name: "Dashboard", href: "#", current: true },
  { name: "Team", href: "#", current: false },
  { name: "Projects", href: "#", current: false },
  { name: "Calendar", href: "#", current: false },
];

export default {
  components: {
    Disclosure,
    DisclosureButton,
    DisclosurePanel,
    Menu,
    MenuButton,
    MenuItem,
    MenuItems,
    BellIcon,
    MenuIcon,
    XIcon,
    Link,
    Head,
  },
  setup() {
    return {
      navigation,
    };
  },
  props: ["user"],
    data() {
    return {
      form: this.$inertia.form({
        first_name: this.$props.user.first_name,
        last_name: this.$props.user.last_name,
        date_of_birth: this.$props.user.date_of_birth,
        age: this.$props.user.age,
        home_phone: this.$props.user.home_phone,
        mobile_phone: this.$props.user.mobile_phone,
        email: this.$props.user.email,
        street_address: this.$props.user.street_address,
        city: this.$props.user.city,
        state: this.$props.user.state,
        zip_code: this.$props.user.zip_code,
        current_time: this.$props.user.current_time,
      }),
    };
  },
  methods: {
    submit() {
      this.form.put(this.route("users.update",this.$props.user.id), {});
    },
  },
};
</script>
